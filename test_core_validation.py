def test_company_alias_matching():
    """Test company alias matching functionality"""
    logger = setup_logging()
    logger.info("üß™ Testing Company Alias Matching System")
    logger.info("=" * 60)
    
    try:
        from utils.company_alias_matcher import get_company_matcher
        
        # Initialize company matcher with debug enabled
        matcher = get_company_matcher(debug=False)
        
        logger.info(f"‚úÖ Company matcher initialized with {len(matcher.company_mappings)} companies")
        logger.info(f"üìä Total aliases: {sum(len(aliases) for aliases in matcher.company_mappings.values())}")
        
        # Test specific Microsoft ‚Üí Azure mapping as requested
        test_texts = [
            "Microsoft announced Azure price increases affecting Office365 users",
            "Teams integration with M365 shows strong adoption",
            "Dell PowerEdge servers with VMware vSphere deployment",
            "Cisco Meraki and Umbrella security integration",
            "TD Synnex (formerly Tech Data) AWS partnership"
        ]
        
        logger.info(f"\nüîç Testing {len(test_texts)} sample texts for alias detection:")
        
        total_detections = 0
        microsoft_azure_test_passed = False
        
        for i, text in enumerate(test_texts, 1):
            logger.info(f"\nüìÑ Test {i}: {text}")
            
            # Find companies in text
            result = matcher.find_companies_in_text(text)
            
            logger.info(f"   üéØ Companies detected: {', '.join(sorted(result.matched_companies)) if result.matched_companies else 'None'}")
            
            for company in sorted(result.matched_companies):
                aliases_found = result.alias_hits.get(company, [])
                logger.info(f"      ‚Ä¢ {company}: {', '.join(aliases_found) if aliases_found else 'main name used'}")
                
                # Check for Microsoft‚ÜíAzure mapping
                if company == 'microsoft' and any('azure' in alias.lower() for alias in aliases_found):
                    microsoft_azure_test_passed = True
                    logger.info(f"      ‚úÖ CONFIRMED: Azure alias correctly mapped to Microsoft")
            
            total_detections += len(result.matched_companies)
            logger.info(f"   üìä Confidence: {result.confidence_score:.2f}")
        
        logger.info(f"\nüìä ALIAS MATCHING SUMMARY:")
        logger.info(f"   ‚Ä¢ Total company detections: {total_detections}")
        logger.info(f"   ‚Ä¢ Microsoft‚ÜíAzure mapping: {'‚úÖ PASSED' if microsoft_azure_test_passed else '‚ùå FAILED'}")
        
        return total_detections > 0 and microsoft_azure_test_passed
        
    except Exception as e:
        logger.error(f"‚ùå Company alias matching failed: {e}")
        import traceback
        logger.error(traceback.format_exc())
        return False

